class Solution:
    def isValid(self, s: str) -> bool:
        stack = []
        for i in range(len(s)):
            c = s[i]
            if c == '(':
                stack.append(')')
            elif c == '[':
                stack.append(']')
            elif c == '{':
                stack.append('}')
            elif not stack or stack.pop() != c:
                return False
        return not stack
